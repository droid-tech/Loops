package com.formation.java;

public class SimpleForLoop {

    public static void main(String[] args) {

        /**
         * @Syntax
         * 1- Initialization
         * 2- Condition
         * 3- Incr/Decr
         *
         * for(initialization;condition;incr/decr){
         *     code to be executed...
         * }
         */

        /**
         * @Example 1
         */
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
        /**
         * @EndExample
         */

    }
}
