package com.formation.java;

public class WhileLoop {

    public static void main(String[] args) {

        /**
         * @Syntax
         * while(condition){
         *  code to be executed
         * }
         */

        /**
         * @Example
         */
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        /**
         * @EndExample
         */

        /**
         * @Bonus
         * boucle infini
         * while(true){
         *  code...
         * }
         */
    }

}
