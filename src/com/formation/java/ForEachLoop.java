package com.formation.java;

public class ForEachLoop {

    public static void main(String[] args) {

        /**
         * @Syntax
         *
         * for(Type var : array){
         *  code to be executed
         * }
         */

        /**
         * @Example
         */
        int arr[] = {12, 23, 44, 56, 78};

        for (int i : arr) {
            System.out.println(i);
        }
        /**
         * @EndExample
         */

        /**
         * @Bonus
         * boucle infini
         * for(;;){
         *  code...
         * }
         */

    }

}
