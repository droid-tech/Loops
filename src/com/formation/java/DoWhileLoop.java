package com.formation.java;

public class DoWhileLoop {

    public static void main(String[] args) {

        /**
         * @Syntax:
         * do{
         *  code to be executed
         * } while(condition);
         */

        /**
         * @Example
         */
        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);
        /**
         * @EndExample
         */

    }

}
